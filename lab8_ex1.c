#include <stdio.h>
#include <stdlib.h>

int main() {
    int n = 0;
    int a[100]; // Резервуємо місце під масив
    int *ptr = a; // Вказівник на початок масиву
    int choice;

    do {
        printf("\n--- МЕНЮ ПРОГРАМИ ---\n");
        printf("1. Ввести дані масиву\n");
        printf("2. Показати адреси елементів та знайти кількість ділянок спадання (3 елементи)\n");
        printf("0. Вихід\n");
        printf("Ваш вибір: ");
        scanf("%d", &choice);

        switch (choice) {
            case 1:
                printf("\nВведіть кількість елементів (n): ");
                scanf("%d", &n);

                if (n < 3) {
                    printf("Помилка: потрібно мінімум 3 елементи для аналізу ділянок.\n");
                    n = 0;
                } else {
                    printf("Введіть %d елементів:\n", n);
                    for (int i = 0; i < n; i++) {
                        printf("a[%d] = ", i);
                        // Використання вказівника для введення даних 
                        scanf("%d", (ptr + i));
                    }
                }
                break;

            case 2:
                if (n == 0) {
                    printf("\nСпочатку введіть дані (пункт 1).\n");
                } else {
                    printf("\nАдреси елементів у пам'яті:\n");
                    for (int i = 0; i < n; i++) {
                        // Вивід адреси за допомогою %p 
                        printf("a[%d]: значення = %d, адреса = %p\n", i, *(ptr + i), (void*)(ptr + i));
                    }

                    int count = 0;
                    // Алгоритм пошуку ділянок за допомогою вказівників 
                    for (int i = 0; i <= n - 3; i++) {
                        // Перевірка умови спадання: поточний > наступний > через один 
                        if (*(ptr + i) > *(ptr + i + 1) && *(ptr + i + 1) > *(ptr + i + 2)) {
                            count++;
                        }
                    }
                    printf("\nРезультат: Кількість ділянок = %d\n", count);
                }
                break;

            case 0:
                printf("Вихід з програми\n");
                break;

            default:
                printf("Невірний вибір, спробуйте ще раз.\n");
        }
    } while (choice != 0); 

    return 0;
}