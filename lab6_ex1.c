#include <stdio.h>

int main() {
    int choice;

    do {
        // --- МЕНЮ ---
        printf("\n========================================\n");
        printf("       РОБОТА З МАТРИЦЯМИ            \n");
        printf("========================================\n");
        printf("1. Ввести матрицю та знайти суму максимумів\n");
        printf("0. Вийти з програми\n");
        printf("========================================\n");
        printf("Ваш вибір: ");
        scanf("%d", &choice);

        if (choice == 1) {
            int n, m;

            // Перевірка кількості рядків
            do {
                printf("\nВведіть кількість рядків (n > 0): ");
                scanf("%d", &n);
                if (n <= 0) printf("Помилка! Введіть додатне число.\n");
            } while (n <= 0);

            // Перевірка кількості стовпців
            do {
                printf("Введіть кількість стовпців (m > 0): ");
                scanf("%d", &m);
                if (m <= 0) printf("Помилка! Введіть додатне число.\n");
            } while (m <= 0);

            int a[n][m];
            int total_sum = 0;

            // Введення елементів
            printf("\nВведіть елементи матриці:\n");
            for (int i = 0; i < n; i++) {
                for (int j = 0; j < m; j++) {
                    printf("a[%d][%d] = ", i, j);
                    scanf("%d", &a[i][j]);
                }
            }

            // Наша матриця
            printf("\nВведена матриця:\n");
            for (int i = 0; i < n; i++) {
                printf("| ");
                for (int j = 0; j < m; j++) {
                    printf("%4d ", a[i][j]); // %4d робить гарні відступи для чисел
                }
                printf(" |\n");
            }

            // Пошук суми найбільших елементів
            printf("\nАналіз рядків:\n");
            for (int i = 0; i < n; i++) {
                int max = a[i][0]; 
                for (int j = 1; j < m; j++) {
                    if (a[i][j] > max) {
                        max = a[i][j];
                    }
                }
                printf("Рядок %d: найбільший елемент = %d\n", i, max);
                total_sum += max;
            }

            printf("\n>>> Загальна сума максимумів: %d <<<\n", total_sum);

        } else if (choice != 0) {
            printf("\n[!] Невірна команда. Спробуйте ще раз.\n");
        }

    } while (choice != 0);

    printf("\nПрограма завершена. До зустрічі!\n");
    return 0;
}
